// =============================== BLACK ===============================
// =====================================================================

const black = document.querySelector(".black")

function corBlack(){
    $("body").css({"background-color": "black"})
    $("header").css({"background-color": "black"})
    $(".newList").css({"background-color": "black"})
}


black.addEventListener("click", corBlack)

